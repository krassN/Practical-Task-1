example = 'Топинамбур'

# первый символ строки
print(example[0])

# последний символ строки
print(example[-1])

# вторая половина строки с нечётным количеством символов
print(example[5:10])

# слово наоборот.
print(example[::-1])

# каждый второй символ этой строки
print(example[1:10:2])
